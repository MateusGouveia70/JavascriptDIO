// Tipos de funcao

// Declarativas
function funcao() {
  console.log('Sou uma funcao declarativa')
}

funcao()

// Expressao de funcao
// com nomeacao
var funcao2 = function funcao() {
  console.log('Sou uma mensagem de funcao de exprecao')
}

// sem nomecao
var funcao3 = function () {
  console.log('Sou uma mensagem de funcao de exprecao sem nome')
}

// Arrow function
var funcao4 = () => {
  console.log('Sou uma arrow function')
}

funcao2()
funcao3()
funcao4()
